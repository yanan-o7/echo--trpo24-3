import socket


# =========================
# НАСТРОЙКИ СЕРВЕРА
# =========================

# HOST = '' или '0.0.0.0' означает:
# сервер будет принимать подключения на всех сетевых интерфейсах компьютера.
# Для локальной проверки это тоже подойдет.
HOST = '0.0.0.0'

# Порт, на котором сервер будет ждать подключения клиентов.
PORT = 9090

# Размер порции данных для чтения.
# По условию задания сервер должен принимать строку порциями по 1 КБ.
# 1 КБ = 1024 байта.
BUFFER_SIZE = 1024


def main():
    # Служебное сообщение о запуске сервера
    print('[СЕРВЕР] Запуск сервера...')

    # Создаем TCP-сокет.
    # AF_INET  -> используем IPv4
    # SOCK_STREAM -> используем TCP
    server_socket = socket.socket(socket.AF_INET, socket.SOCK_STREAM)

    try:
        # Разрешаем повторное использование адреса.
        # Это удобно при отладке:
        # если вы только что остановили сервер и тут же запускаете снова,
        # порт может быть еще "занят" системой.
        server_socket.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 1)

        # Привязываем сокет к адресу и порту.
        # После этого ОС знает, что именно этот процесс обслуживает PORT.
        server_socket.bind((HOST, PORT))

        print(f'[СЕРВЕР] Сервер привязан к адресу {HOST} и порту {PORT}.')

        # Переводим сокет в режим ожидания входящих подключений.
        # Число 1 означает размер очереди ожидания подключений.
        server_socket.listen(1)

        # Служебное сообщение о начале прослушивания порта
        print(f'[СЕРВЕР] Начато прослушивание порта {PORT}. Ожидание клиента...')

        # accept() блокирует выполнение программы до тех пор,
        # пока кто-то не подключится к серверу.
        #
        # В результате accept() возвращает:
        # 1) conn  - новый сокет для общения с конкретным клиентом
        # 2) addr  - адрес клиента в виде кортежа (ip, port)
        conn, addr = server_socket.accept()

        # Служебное сообщение о подключении клиента
        print(f'[СЕРВЕР] Подключение клиента: IP = {addr[0]}, PORT = {addr[1]}')

        # Будем накапливать весь текст клиента здесь,
        # чтобы при желании можно было увидеть полное сообщение целиком.
        full_message = ''

        # Конструкция with гарантирует корректное закрытие клиентского сокета,
        # даже если во время работы возникнет ошибка.
        with conn:
            # Бесконечный цикл приема данных от клиента
            while True:
                # Получаем до 1024 байт данных.
                # Если данных пришло меньше - recv вернет меньше.
                # Если клиент закрыл отправку, recv вернет b''.
                data = conn.recv(BUFFER_SIZE)

                # Если пришел пустой набор байтов,
                # значит клиент завершил отправку данных.
                if not data:
                    print('[СЕРВЕР] Клиент отключился.')
                    break

                # Преобразуем байты в строку.
                # decode('utf-8') превращает байты в обычный текст Python.
                text = data.decode('utf-8')

                # Добавляем принятую часть к полной строке
                full_message += text

                # Служебное сообщение о приеме данных
                print(f'[СЕРВЕР] Принято {len(data)} байт от клиента: {text!r}')

                # Отправляем клиенту обратно те же самые данные.
                # Это и есть логика эхо-сервера.
                #
                # sendall() лучше, чем send():
                # send() может отправить не все байты сразу,
                # а sendall() пытается отправить ВСЕ переданные данные.
                conn.sendall(data)

                # Служебное сообщение об отправке данных
                print(f'[СЕРВЕР] Отправлено {len(data)} байт клиенту: {text!r}')

        # Необязательный, но полезный вывод:
        # показываем всю строку, собранную из всех кусков.
        print(f'[СЕРВЕР] Полностью полученная строка: {full_message!r}')

    except KeyboardInterrupt:
        # Это сработает, если вы остановите сервер через Ctrl+C
        print('\n[СЕРВЕР] Получена команда на остановку с клавиатуры (Ctrl+C).')

    except Exception as error:
        # Общий обработчик ошибок, чтобы новичку было проще увидеть,
        # если что-то пошло не так.
        print(f'[СЕРВЕР] Ошибка: {error}')

    finally:
        # Этот блок выполнится в любом случае:
        # и при нормальном завершении, и при ошибке.
        server_socket.close()

        # Служебное сообщение об остановке сервера
        print('[СЕРВЕР] Остановка сервера.')


# Точка входа в программу.
# Этот код выполнится только если файл запущен напрямую:
# python server.py
if __name__ == '__main__':
    main()
